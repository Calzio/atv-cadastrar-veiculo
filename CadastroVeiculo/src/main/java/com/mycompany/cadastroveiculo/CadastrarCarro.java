    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
    String marcaBusca = txtMarcaCarro.getText().trim();
    String modeloBusca = txtModeloCarro.getText().trim();

    if (marcaBusca.isEmpty() || modeloBusca.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
        return;
    }

    boolean removido = false;

    for (int i = 0; i < listaCarro.size(); i++) {
        Carro c = listaCarro.get(i);
        if (c.getMarca().equalsIgnoreCase(marcaBusca) && c.getModelo().equalsIgnoreCase(modeloBusca)) {
            listaCarro.remove(i);
            removido = true;
            break;
        }
    }

    if (removido) {
        JOptionPane.showMessageDialog(null, "Carro removido com sucesso!");
        txtArea.setText(""); // Limpa a Ã¡rea de texto
        for (Carro c : listaCarro) {
            txtArea.append(c.toString() + "\n");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Carro nÃ£o encontrado.");
    }

    // Limpar campos
    txtMarcaCarro.setText("");
    txtModeloCarro.setText("");
    txtNPortas.setText("");
    }//GEN-LAST:event_btnRemoverActionPerformed

