    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
    String marcaBusca = txtMarcaCarro.getText().trim();
    String modeloBusca = txtModeloCarro.getText().trim();
    String nPortasText = txtNPortas.getText().trim();

    if (marcaBusca.isEmpty() || modeloBusca.isEmpty() || nPortasText.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
        return;
    }

    try {
        int novaNPortas = Integer.parseInt(nPortasText);
        boolean encontrado = false;

        for (Carro c : listaCarro) {
            if (c.getMarca().equalsIgnoreCase(marcaBusca) && c.getModelo().equalsIgnoreCase(modeloBusca)) {
                c.setNPortas(novaNPortas); // MÃ©todo para alterar o nÃºmero de portas
                encontrado = true;
                break;
            }
        }

        if (encontrado) {
            JOptionPane.showMessageDialog(null, "Dados do carro atualizados com sucesso!");
            txtArea.setText(""); // Limpa a Ã¡rea de texto
            for (Carro c : listaCarro) {
                txtArea.append(c.toString() + "\n");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Carro nÃ£o encontrado.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "NÃºmero de portas invÃ¡lido.");
    }
    }//GEN-LAST:event_btnAlterarActionPerformed

