    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
    try {
        String marca = txtMarcaMoto.getText().trim();
        String modelo = txtModeloMoto.getText().trim();
        String cilindradaText = txtCilindrada.getText().trim();
        
        if (marca.isEmpty() || modelo.isEmpty() || cilindradaText.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
            return;
        }

        int cilindrada = Integer.parseInt(cilindradaText);

        Moto moto = new Moto(marca, modelo, cilindrada);
        listaMoto.add(moto);
        JOptionPane.showMessageDialog(null, "Moto cadastrada com sucesso!");

        // Limpar campos
        txtMarcaMoto.setText("");
        txtModeloMoto.setText("");
        txtCilindrada.setText("");
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Cilindrada inválida. Por favor, insira um número inteiro.");
    }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
    String marcaBusca = txtMarcaMoto.getText().trim();
    String modeloBusca = txtModeloMoto.getText().trim();

    if (marcaBusca.isEmpty() || modeloBusca.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
        return;
    }

    boolean removido = false;

    for (int i = 0; i < listaMoto.size(); i++) {
        Moto m = listaMoto.get(i);
        if (m.getMarca().equalsIgnoreCase(marcaBusca) && m.getModelo().equalsIgnoreCase(modeloBusca)) {
            listaMoto.remove(i);
            removido = true;
            break;
        }
    }

    if (removido) {
        JOptionPane.showMessageDialog(null, "Moto removida com sucesso!");
        txtArea.setText(""); // Limpa a área de texto
        for (Moto m : listaMoto) {
            txtArea.append(m.toString() + "\n");
        }
    } else {
        JOptionPane.showMessageDialog(null, "Moto não encontrada.");
    }

    // Limpar campos
    txtMarcaMoto.setText("");
    txtModeloMoto.setText("");
    txtCilindrada.setText("");
    }//GEN-LAST:event_btnRemoverActionPerformed

