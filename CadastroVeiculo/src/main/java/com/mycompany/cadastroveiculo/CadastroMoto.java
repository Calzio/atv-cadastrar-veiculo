    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
    String marcaBusca = txtMarcaMoto.getText().trim();
    String modeloBusca = txtModeloMoto.getText().trim();
    String cilindradaText = txtCilindrada.getText().trim();

    if (marcaBusca.isEmpty() || modeloBusca.isEmpty() || cilindradaText.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
        return;
    }

    try {
        int novaCilindrada = Integer.parseInt(cilindradaText);
        boolean encontrado = false;

        for (Moto m : listaMoto) {
            if (m.getMarca().equalsIgnoreCase(marcaBusca) && m.getModelo().equalsIgnoreCase(modeloBusca)) {
                m.setCilindrada(novaCilindrada);
                encontrado = true;
                break;
            }
        }

        if (encontrado) {
            JOptionPane.showMessageDialog(null, "Dados da moto atualizados com sucesso!");
            txtArea.setText(""); // Limpa a área de texto
            for (Moto m : listaMoto) {
                txtArea.append(m.toString() + "\n");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Moto não encontrada.");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "Cilindrada inválida.");
    }
    }//GEN-LAST:event_btnAlterarActionPerformed

